package desktop.ifnu.bima.feel.of.java.operator.operatorassignment;

public class Example {
    public static void main(String[] args){
        int x = 5;
        x = x + 100;
        x = x -1;

        int y = 10;
        y = y * 3;
        y = y / 10;

        // bisa disingkat menjadi
        x += 100;
        x -= 1;
        y *= 3;
        y /= 10;
        // sebenarnya operator campuran ada sebelas(11) buah, tapi hanya empat di atas saja yang sering digunakan

    }

}
