package desktop.ifnu.bima.feel.of.java.operator.aritmatika;

public class IncrementTest {
  public static void main(String[] args) {
    int x = 10;
    System.out.println("x++ = " + x++);
    System.out.println("Setelah evaluasi, x = " + x);
    x = 10;
    System.out.println("++x = " + ++x);
    System.out.println("Setelah evaluasi, x = " + x);
  }
}
