public class Keluarga {

    private String kake;
    private String nenek;
    private String bibi;
    private String uwa;
    private String mamang;

    public String getKake() {
        return kake;
    }

    public void setKake(String kake) {
        this.kake = kake;
    }

    public String getNenek() {
        return nenek;
    }

    public void setNenek(String nenek) {
        this.nenek = nenek;
    }

    public String getBibi() {
        return bibi;
    }

    public void setBibi(String bibi) {
        this.bibi = bibi;
    }

    public String getUwa() {
        return uwa;
    }

    public void setUwa(String uwa) {
        this.uwa = uwa;
    }

    public String getMamang() {
        return mamang;
    }

    public void setMamang(String mamang) {
        this.mamang = mamang;
    }
}
