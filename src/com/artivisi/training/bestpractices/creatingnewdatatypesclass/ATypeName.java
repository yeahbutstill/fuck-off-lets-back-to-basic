package com.artivisi.training.bestpractices.creatingnewdatatypesclass;

// secara historis, sebagian besar bahasa berorientasi objek telah
// menggunakan kata kunci class yang berarti “Saya akan memberitahu Anda seperti apa jenis objek baru itu.”
// Kata kunci class diikuti dengan nama tipe baru. Misalnya:
public class ATypeName {
    /*
    Class body goes here
     */
    // Anda dapat membuat objek jenis ini menggunakan kata kunci new
    // Namun Anda tidak dapat memerintahkannya untuk melakukan banyak hal
    // (yaitu, Anda tidak dapat mengirimkan pesan menarik apa pun) sampai
    // Anda menentukan beberapa metode untuknya.
    ATypeName a = new ATypeName();
}