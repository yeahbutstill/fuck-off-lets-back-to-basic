package com.zamannow.testsoaljava.junior.day1;

public class PseudocodeWindow {
    public static void main(String[] args) {
        for (int i = 0; i < 2; i++) {
            for (int j = 0; j < 1; j++) {
                System.out.println("+===+===+");
            }

            for (int k = 0; k < 3; k++) {
                System.out.println("|   |   |");
            }
        }
        System.out.println("+===+===+");
    }
}
