package com.zamannow.testsoaljava.junior.day1;

public class BJP3SelfCheck310printStrings {
    public static void main(String[] args) {
        printStrings("abc", 5);
    }

    public static void printStrings(String a, int b) {
        for (int i = 0; i < b; i++) {
            System.out.print(a);
        }
    }
}
