package com.zamannow.basic.programming.model.primitif.data.type;

public class ConditionalIfElse {
  public static void main(String[] args) {
    int x = 10;
    int y = 11;
    int max;
    if (x > y) {
      max = x;
      System.out.println(max);
    } else {
      max = y;
      System.out.println(max);
    }
  }
}
