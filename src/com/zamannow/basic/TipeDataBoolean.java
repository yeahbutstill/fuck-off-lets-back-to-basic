package com.zamannow.basic;

public class TipeDataBoolean {
    public static void main(String[] args) {
        boolean benar = true;
        boolean salah = false;

        System.out.println(salah);
        System.out.println(benar);
        // kebalikannya
        System.out.println(!salah);
        System.out.println(!benar);
    }
}
