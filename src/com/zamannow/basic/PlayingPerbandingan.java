package com.zamannow.basic;

public class PlayingPerbandingan {
    public static void main(String[] args) {
        int val1 = 10;
        int val2 = 1020;

        // operasi perbandingan akan selalu return atau mengembalikan tipe boolean
        System.out.println(val1 > val2);
        System.out.println(val1 < val2);
        System.out.println(val1 >= val2);
        System.out.println(val1 <= val2);
        System.out.println(val1 == val2);
        System.out.println(val1 != val2);
        System.out.println();
        System.out.println("=====================================");

        boolean b = val2 == val1;
        System.out.println(b);
        System.out.println("========================================");
        System.out.println();


    }
}
