package id.toki.tlx.algo;

public class EscapeSequence {
  public static void main(String[] args) {
    System.out.print("tab\t");
    System.out.print("backspace\b");
    System.out.print("baris baru\n");
    System.out.print("carriage return\r");
    System.out.print("form feed\f");
    System.out.print("single quote\'");
    System.out.print("double quote\"");
    System.out.print("backslash\\");
  }
}
