package id.toki.tlx.algo;

public class PerulanganForSatu {
    public static void main(String[] args) {
        System.out.println("Perulangan Menggunakan For");
        System.out.println("==============================");
        for (int x = 5; x <= 25; x += 5) {
            System.out.println("Angka Perulangan: " + x);
        }
    }
}
