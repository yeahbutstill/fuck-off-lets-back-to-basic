package id.toki.tlx.algo;

public class Contoh1a {
  public static void main(String[] args) {
    int x;
    int y;
    int c;
    x = 4 & 6;
    y = 5 >>> 3;
    int y1 = 5 << 2;
    int y2 = 5 >> 2;
    c = (2 + 4) * 6;

    System.out.println(x);
    System.out.println(y);
    System.out.println(y1);
    System.out.println(y2);
    System.out.println(c);
  }
}
