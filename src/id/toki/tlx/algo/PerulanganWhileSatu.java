package id.toki.tlx.algo;

public class PerulanganWhileSatu {
  public static void main(String[] args) {
    int i = 1;
    System.out.println("Perulangan Menggunakan While");
    System.out.println("=============================");
    while (i <= 10) {
      System.out.println("Angka Perulangan : " + i);
      i++;
    }
  }
}
