package id.toki.tlx.algo;

public class Aritmatika {
    static int a = 10;
    static int b = 11;

    public static void main(String[] args) {
        System.out.println("menjumlahkan 2 buah nilai a + b = " + (a+b));
        System.out.println("mengurangkan 2 buah nilai a - b = " + (a-b));
        System.out.println("mengalihkan 2 buah nilai a * b = " + (a*b));
        System.out.println("membagi 2 buah nilai a / b = " + (a/b));
        System.out.println("memberikan sisa hasil bagi a % b = " + (a%b));
        System.out.println("menaikan nilai dari variable sebanyak 1 = " + (++a));
        System.out.println("menurunkan nilai dari variable sebanyak 1 = " + (--a));
    }
}
